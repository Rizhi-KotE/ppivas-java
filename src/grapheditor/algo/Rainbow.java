	public static Color getColor(double coef) {
		if (coef > 1) {
			return Color.blue;
		}
		if (coef < 0) {
			return Color.red;
		}
		int num = (int) (coef * rgb.length);
		if (num < rgb.length) {
			return new Color(rgb[num],false);
		} else {
			return Color.BLUE;
		}
	}
